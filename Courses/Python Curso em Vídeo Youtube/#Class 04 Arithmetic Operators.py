n1 = int(input('Enter a number: '))
n2 = int(input('Enter another number: '))
print(f'The sum is: {n1 + n2}')
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print(f'The sum is: {s}, the multiplication is: {m}, the division is: {d:.3f}', end=' ')
print(f' The floor division is: {di} and the exponential is: {e}')

